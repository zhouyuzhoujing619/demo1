  <h2>Just about me</h2>
    <ul> 
     <p>&nbsp;&nbsp;&nbsp;&nbsp;一直以来，身边的人对于我想要从事的职业很是不解，为什么一个女孩子非要天天面对着电脑不停的敲敲打打，难道不应该真的回家当个老师或者考个公务员，天天按时上班，按点下班吗？这样整天面对电脑工作，迟早会变成一个脾气古怪的老妖婆。</p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;也许我真的可以按照世俗的想法而活，可是我不甘心，我不想在我二十岁本该有美好未来的时候一眼看到了五六十岁的生活，天天备课上课，天天老公孩子，也许这才是生活的悲剧吧，我希望在我看不到的将来，每天有惊喜等着我，也许仅仅是调试出了一个小bug，可是这种成就感不是每天刷刷微博，建建文档能给予的......</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;每个人的人生生来不同，像我的室友，大学四年宅了四年寝室，整天躺在床上看电视，叫外卖，一直的工作追求就是以后找个安稳的工作，找个老公，生个娃，然后做起全职妈妈。有的励志考研，将来找一份好工作。所以大家都互相不理解，一个女孩子为什么要这么拼？我却认为一个女孩子为什么要将自己最好的年华浑浑噩噩度过，将来将自己的幸福安全依赖于自己的老公身上。可能个人的生活经历不一样吧，在我的概念里，面包要比爱情重要。</p>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;大学逃过课，熬过夜，但是这些时间都被我花在了工作室，我不后悔我的大学三年这样度过。选择进入工作室是我大学最明智的选择，如果说有什么遗憾的话，就是在最开始进去的时候不知方向，有点度日子，浪费了太多时间，如果重来，一定会抓紧更多的时间来学习。不过古语说得好：种一棵树最好的时间有两个，一个是十年前，一个就是现在。所以不用抱怨太多，从现在开始，脚踏实地的学习，每一天都在进步，这样就是极好的。
</p>
